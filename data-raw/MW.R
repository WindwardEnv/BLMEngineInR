# summing atomic weights to get molecular weights
MW = c(
  MW,
  SO4 = as.numeric(MW["S"] + 4 * MW["O"]),
  NH4 = as.numeric(MW["N"] + 4 * MW["H"]),
  NO3 = as.numeric(MW["N"] + 3 * MW["O"]),
  NO2 = as.numeric(MW["N"] + 2 * MW["O"]),
  PO4 = as.numeric(MW["P"] + 4 * MW["O"]),
  CO2 = as.numeric(MW["C"] + 2 * MW["O"]),
  CaCO3 = as.numeric(MW["Ca"] + MW["C"] + 3 * MW["O"]),
  CaO = as.numeric(MW["Ca"] + MW["O"]),
  CO3 = as.numeric(MW["C"] + 3 * MW["O"]),
  HCO3 = as.numeric(MW["H"] + MW["C"] + 3 * MW["O"]),
  H2CO3 = as.numeric(2 * MW["H"] + MW["C"] + 3 * MW["O"]),
  H2O = as.numeric(2 * MW["H"] + MW["O"]),
  OH = as.numeric(MW["O"] + MW["H"])
)

# save data
